/* -------------------------------------------------------------------------- */
/*								   imports								  */
/* -------------------------------------------------------------------------- */
import mongoose from 'mongoose';

/* -------------------------------------------------------------------------- */

/* -------------------------------------------------------------------------- */
/*								   schema								   */
/* -------------------------------------------------------------------------- */
const Schema = new mongoose.Schema({
	answerSheetId: {
		type: String,
		required: true,
		unique: false,
		default: '',
	},
	marks: {
		type: Boolean,
		required: true,
		unique: true,
		default: true,
	}
},
{
	timestamps: false,
},
);
/* -------------------------------------------------------------------------- */


/* -------------------------------------------------------------------------- */
/*								model export								*/
/* -------------------------------------------------------------------------- */
export default Schema;
/* -------------------------------------------------------------------------- */
