/* -------------------------------------------------------------------------- */
/*								   imports								  */
/* -------------------------------------------------------------------------- */
import mongoose from 'mongoose';

/* -------------------------------------------------------------------------- */

/* -------------------------------------------------------------------------- */
/*								   schema								   */
/* -------------------------------------------------------------------------- */
const Schema = new mongoose.Schema({
	name: {
		type: String,
		required: true,
		unique: false,
		default: '',
	},
    program: {
		type: String,
		required: true,
		unique: false,
		default: '',
	},
    level: {
		type: Number,
		enum: [1, 2, 3, 4, 5, 6],
		required: true,
		unique: false,
		index: false,
		default: '',
	},
    studentIds:[ {
		type: String,
		required: true,
		unique: false,
		default: '',
	}]
},
{
	timestamps: false,
},
);
/* -------------------------------------------------------------------------- */


/* -------------------------------------------------------------------------- */
/*								model export								*/
/* -------------------------------------------------------------------------- */
export default Schema;
/* -------------------------------------------------------------------------- */
