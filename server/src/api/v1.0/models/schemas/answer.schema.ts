/* -------------------------------------------------------------------------- */
/*								   imports								  */
/* -------------------------------------------------------------------------- */
import mongoose from 'mongoose';

/* -------------------------------------------------------------------------- */

/* -------------------------------------------------------------------------- */
/*								   schema								   */
/* -------------------------------------------------------------------------- */
const Schema = new mongoose.Schema({
	body: {
		type: String,
		required: true,
		default: '',
	},
	isCorrect: {
		type: Boolean,
		required: true,
		unique: false,
		default: false,
	},
    questionId:{
		type: String,
		required: true,
		default: '',
	},
},
{
	timestamps: false,
},
);
/* -------------------------------------------------------------------------- */


/* -------------------------------------------------------------------------- */
/*								model export								*/
/* -------------------------------------------------------------------------- */
export default Schema;
/* -------------------------------------------------------------------------- */
